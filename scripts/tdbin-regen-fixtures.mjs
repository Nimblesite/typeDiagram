// Regenerates the four committed GENERATED tdbin crate modules from their
// single-source `.td` schemas, then reformats the crate. Run from the repo
// root after any rust-tdbin codegen change:
//
//   node scripts/tdbin-regen-fixtures.mjs
//
// The vitest drift guards (rust-tdbin.test.ts, rust-tdbin-corpus.test.ts)
// read the SAME schema files, so committed modules and pinned expectations
// can never disagree about the source of truth.
import { execFileSync } from "node:child_process";
import { readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const root = process.cwd();
const run = (command, args) => execFileSync(command, args, { cwd: root, stdio: "inherit" });

run("npm", ["run", "-w", "typediagram-core", "build"]);

const dist = join(root, "packages/typediagram/dist");
const { parse } = await import(join(dist, "parser/index.js"));
const { buildModel } = await import(join(dist, "model/index.js"));
const { generateRustModule } = await import(join(dist, "converters/rust-tdbin.js"));

const fail = (context, error) => {
  console.error(`tdbin-regen-fixtures: ${context}`, error);
  process.exit(1);
};

const generate = (tdPath, layout) => {
  const source = readFileSync(join(root, tdPath), "utf8");
  const parsed = parse(source);
  if (!parsed.ok) {
    fail(`parse ${tdPath}`, parsed.error);
  }
  const model = buildModel(parsed.value);
  if (!model.ok) {
    fail(`model ${tdPath}`, model.error);
  }
  const generated = generateRustModule(model.value, { layout });
  if (!generated.ok) {
    fail(`generate ${tdPath} (layout ${layout})`, generated.error);
  }
  return generated.value;
};

