for (const fixture of FIXTURES) {
  const body = generate(fixture.td, fixture.layout);
  writeFileSync(join(root, fixture.out), `${fixture.header}\n${fixture.marker}\n${body}`);
  console.log(`regenerated ${fixture.out} (layout ${fixture.layout})`);
}

